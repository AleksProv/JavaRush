package level4.lectore_13;

//Вывести горизонтальную и вертикальную линию из 10 восьмерок через for.

public class Solution_4 {
    public static void main (String[] args) {
        for (int i = 0; i < 10; i++)
            System.out.print (8);
        System.out.println (" ");
        for (int j = 0; j < 10; j++)
            System.out.println (8);
    }
}
